// 给字符串切片数组排序
func SortStringSlice(strs []string) []string {
	for i := 0; i < len(strs); i++ {
		for j := i + 1; j < len(strs); j++ {
			if strs[i] > strs[j] {
				strs[i], strs[j] = strs[j], strs[i]
				fmt.Println(strs)
			}
		}
	}
	return strs
}

